

module.exports = class Node {
  constructor(value) {
    this.value = value;
    this.right = null;
    this.left = null;
  }
  getvalue() {
    return this.value;
  }
  getRight() {
    return this.getRight;
  }
  getLeft() {
    return this.getLeft;
  }
  setRight(node) {
    this.right = node
  }
  setLeft(node) {
    this.left = node
  }
}
